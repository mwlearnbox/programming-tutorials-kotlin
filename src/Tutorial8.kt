fun main(){

    //var are mutable they change
    var price = 20
    print("The Amaze Apples are selling at $")
    println(price)

    price = 250
    print("The Amaze Apples are now selling at $")
    println(price)

    //val are not mutable they do not change
    val tax = 87
    print("The tax is ")
    println(tax)

    //tax = 20 you cannot reassign vals
    //var tax = 20 this cannot happen

}